@bot.message_handler(commands=['start'])
def start(message):
    chat_id = message.chat.id
    # Сохраняем прогресс при рестарте
    existing = user_state.get(chat_id, {})
    user_state[chat_id] = {
        'completed_topics': existing.get('completed_topics', set()),
        'results': existing.get('results', []),
    }
    bot.send_message(
        message.chat.id,
        "🎓 *Добро пожаловать в бот подготовки по Статистике!*\n\n"
        "Здесь ты найдёшь:\n"
        "📚 Теоретические материалы по всем темам\n"
        "🧪 Тесты с объяснением правильных ответов\n"
        "📊 Отслеживание своего прогресса\n\n"
        "Выбери действие в меню ниже 👇",
        parse_mode="Markdown",
        reply_markup=main_menu_keyboard()
    )

